# 8) მომხმარებელს იქამდე შეეკითხეთ რიცხვები სანამ უარყოფით რიცხვს არ შემოიყვანს, while ციკლისა და input ინსტრუქციის საშვალებით, ასევე პირობითი განცხადებების დადებითობა/უარყოფითობის შესამოწმებლად, საბოლოოდ დაბეჭდეთ ყველა მიღებული დადებითი რიცხვის ჯამი

total = 12

while True:
    number = int(input("შეიყვანე რიცხვი: "))
    
    if number < 0:
        print("უარყოფითი რიცხვი შეიყვანე — ციკლი დასრულდა.")
        break
    elif number > 0:
        total += number
        print("ჯამი ამ დროისთვის:", total)
    else:
        print("რიცხვი ნულია — არც ემატება, არც აკლდება ჯამს.")
    print("საბოლოო დადებითი რიცხვების ჯამი არის:", total)