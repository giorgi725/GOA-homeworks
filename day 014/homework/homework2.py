# ალგორითმი — ეს არის ნაბიჯ-ნაბიჯ აღწერილი ინსტრუქციების ერთობლიობა, რომლებიც კონკრეტული პრობლემის გადაჭრას უზრუნველყოფს. ანუ, ალგორითმი გვიჩვენებს:
#1. რა უნდა გავაკეთოთ,
#2. რა თანმიმდევრობით,
#3. რა შედეგს მივიღებთ.
# ალგორითმის წარმოდგენის გზები:
# Natural Language (ბუნებრივი ენა)აღწერილია ჩვეულებრივ სიტყვებით, მარტივად და გასაგებად.
# Pseudocode (ფსევდოკოდი)აღწერილია ბუნებრივი ენით, მაგრამ პროგრამირების სტილში — შუალედური ვარიანტი ტექსტსა და კოდს შორის.
# Flowchart (დინების დიაგრამა)ალგორითმი ნაჩვენებია გრაფიკულად — ბლოკების და ისრების საშუალებით.
