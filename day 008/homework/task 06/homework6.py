#7) integer და float მონაცემთა ტიპის ცვლადებზე შეასრულეთ 20 მაგალითი ოპერატორებით: +, -, *, /, **, %

# (int)
a = 10
b = 3

# (float)
c = 5.5
d = 2.0

# მაგალითები ოპერატორებით 

#(+)
print(a + b)      # 10 + 3 = 13
print(c + d)      # 5.5 + 2.0 = 7.5
print(a + c)      # 10 + 5.5 = 15.5
print(b + d)      # 3 + 2.0 = 5.0
print(a + b + c)  # 10 + 3 + 5.5 = 18.5

# (-)
print(a - b)      # 10 - 3 = 7
print(c - d)      # 5.5 - 2.0 = 3.5
print(a - c)      # 10 - 5.5 = 4.5
print(b - d)      # 3 - 2.0 = 1.0
print(c - a)      # 5.5 - 10 = -4.5

#(*)
print(a * b)      # 10 * 3 = 30
print(c * d)      # 5.5 * 2.0 = 11.0
print(a * d)      # 10 * 2.0 = 20.0

#(/)
print(a / b)      # 10 / 3 = 3.333...
print(c / d)      # 5.5 / 2.0 = 2.75
print(a / d)      # 10 / 2.0 = 5.0

#(** — ხარისხში აყვანა)
print(a ** b)     # 10^3 = 1000
print(d ** b)     # 2.0^3 = 8.0

#(% — ნაშთის მიღება)
print(a % b)      # 10 % 3 = 1
print(c % d)      # 5.5 % 2.0 = 1.5